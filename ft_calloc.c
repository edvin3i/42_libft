#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	int		vol;
	void	*dest;

	vol = (int)(count * size);
	dest = malloc(vol);
	if (dest)
		ft_bzero(dest, vol);
	return (dest);
}
