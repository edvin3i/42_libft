#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*slst;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		slst = *lst;
		*lst = (*lst)->next;
		ft_lstdelone(slst, del);
	}
}
